// using math object in js
//  printing pi value using math object
console.log(Math.PI);

//  coverting in absolute value
let num = -30;
console.log(Math.abs(num));

// ceil method will round up the value to next integer value
let num2 = 11.4;
console.log(Math.ceil(num2));

// floor method will round down the value to previous integer value
console.log(Math.floor(num2));

// generating the random number using math object from 1 to 10
let random = Math.random() * 10 + 1;
console.log(random);
